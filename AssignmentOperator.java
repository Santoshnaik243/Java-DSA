public class AssignmentOperator {
    public static void main(String[] args) {
        int p=10;
        int q;

        // = operator
        q = p;
        System.out.println(q);

        // += operator
        p += q; // p=p+q, p = 10+10=20
        System.out.println(p);

        // -=Operator  p = p-q, p=20-10=10
        p -=q;
        System.out.println(p);

        // *= Operator p=p*q

        p *=q;
        System.out.println(p);

        // /=  Operator p=p/q
        p/=q;
        System.out.println(p);

        




    }
}
